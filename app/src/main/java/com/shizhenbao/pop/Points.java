public class Points {
    private float X;
    private float Y;
    private int i;
    private float show;
    public Points(float x, float y) {
        X = x;
        Y = y;
    }

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public float getShow() {
        return show;
    }

    public void setShow(float show) {
        this.show = show;
    }

    public float getX() {
        return X;
    }

    public void setX(float x) {
        X = x;
    }

    public float getY() {
        return Y;
    }

    public void setY(float y) {
        Y = y;
    }
}
